    public void arithmetic_operations()
    {
        switch(Calculation)
        {
            case 1:
                Answer = Number1 + Float.parseFloat(ResultField.getText());
                ResultField.setText(Double.toString(Answer));
                break;
            case 2:
                Answer = Number1 - Float.parseFloat(ResultField.getText());
                ResultField.setText(Double.toString(Answer));
                break;
            case 3:
                Answer = Number1 * Float.parseFloat(ResultField.getText());
                ResultField.setText(Double.toString(Answer));
                break;
            case 4:
                Answer = Number1 / Float.parseFloat(ResultField.getText());
                ResultField.setText(Double.toString(Answer));
                break;
        }
    }
   
    

