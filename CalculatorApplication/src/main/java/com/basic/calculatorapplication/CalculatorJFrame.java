    private void BackspaceButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BackspaceButtonActionPerformed
        String ResultFieldText = ResultField.getText();
        if(ResultFieldText.equals("1")||ResultFieldText.equals("2")||ResultFieldText.equals("3")||ResultFieldText.equals("4")||ResultFieldText.equals("5")||ResultFieldText.equals("6")||ResultFieldText.equals("7")||ResultFieldText.equals("8")||ResultFieldText.equals("9"))
            {
                    ResultField.setText("");
                    ResultField.setText("0");
            }
        else
        {
            ResultField.setText(ResultFieldText.replace(ResultFieldText.substring(ResultFieldText.length()-1),""));// TODO add your handling code here:
        }
    }//GEN-LAST:event_BackspaceButtonActionPerformed

